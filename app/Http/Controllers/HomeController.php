     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $product = DB::table('items')
            ->join('total_property','items.prd_id','total_property.itemsid')
            ->where('prd_id', $id)->get();
        $images = DB::table('images')
            ->where('itemsid', $id)->get();
        return view('client.product',[
            'product' => $product,
            'images' => $images,
            'id' => $id,
        ]);
    }

